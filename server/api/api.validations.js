'use strict'

const Joi = require('joi')

const validations = {
  // Games list validation
  listGames: {
    payload: Joi.object({
      currency: Joi.string().required(),
      agent_id: Joi.string().required()
    }),
    options: { allowUnknown: true }
  },

  // Single login validation
  singleLogin: {
    payload: Joi.object({
      agent_id: Joi.string().required().description('Agent ID/Merchant'),
      game_id: Joi.string().required().description('Hotdog game ID (use "lobby" for lobby, or specific game ID like "hd-bull-bull")'),
      player_id: Joi.string().required().description('Player ID from Agent'),
      session_id: Joi.string().required().description('Unique session ID generated by Agent'),
      language: Joi.string().required().description('Language code'),
      currency: Joi.string().required().description('Currency code'),
      return_url: Joi.string().allow('').optional().description('Redirect URL when player exits game')
    }),
    options: { allowUnknown: true }
  },

  generateSignature: {
    payload: Joi.object({
      agent_id: Joi.string().required().description('Agent ID'),
      game_id: Joi.string().required().description('Game ID'),
      player_id: Joi.string().required().description('Player ID'),
      session_id: Joi.string().required().description('Session ID'),
      language: Joi.string().required().description('Language'),
      currency: Joi.string().required().description('Currency'),
      return_url: Joi.string().allow('').optional().description('Return URL')
    }).description('Request payload for signature generation'),
    options: {
      allowUnknown: false
    }
  }
}

module.exports = validations 